/**
 * 브랜드 ID가 시스템 예약어에 속하는지 여부를 검사합니다.
 */
export function isReservedBrandId(brandId: string): boolean {
  if (!brandId) return false;
  const cleanId = brandId.trim().toLowerCase();
  return RESERVED_BRAND_IDS.includes(cleanId);
}

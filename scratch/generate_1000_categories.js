function generateCategories() {
  const categories = [];
  let globalId = 1;

  console.log("Generating 2,400 Category Data Points (12 Primaries * 10 Secondaries * 10 Tertiaries * 2 Seasons)...");

  // 대분류 순회 (총 12개 대분류)
  const primaries = Object.keys(categoryTree); 

  primaries.forEach((primary) => {
    const secondaries = Object.keys(categoryTree[primary]); // 대분류당 10개 중분류 (총 120개 중분류)
    
    secondaries.forEach((secondary) => {
      const tertiaries = categoryTree[primary][secondary]; // 중분류당 10개 소분류 (총 1200개 소분류)
      
      tertiaries.forEach((tertiary) => {
        // 기본 1,200개 조합 생성
        categories.push({
          id: globalId++,
          primary: primary,
          secondary: secondary,
          tertiary: tertiary
        });
      });
    });
  });

  // 봄/여름(SS) 및 가을/겨울(FW) 시즌 바리에이션 결합하여 정확히 2,400개 행으로 확장
  const variations = [
    { suffix: " (봄/여름 시즌)" },
    { suffix: " (가을/겨울 시즌)" }
  ];

  const finalCategories = [];
  let finalId = 1;

  categories.forEach((item) => {
    variations.forEach((v) => {
      finalCategories.push({
        id: finalId++,
        primary: item.primary,
        secondary: item.secondary,
        tertiary: item.tertiary + v.suffix
      });
    });
  });

  console.log(`Generated exactly ${finalCategories.length} category items!`);

  // JSON 파일로 쓰기 (categories_1000.json 파일에 모아서 덮어쓰기)
  const targetPath = path.join(__dirname, "categories_1000.json");
  fs.writeFileSync(targetPath, JSON.stringify(finalCategories, null, 2), "utf8");

  console.log(`Successfully wrote 2,400 categories data to: ${targetPath}`);
}

generateCategories();
